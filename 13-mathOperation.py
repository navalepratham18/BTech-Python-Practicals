import math

print("Square root of 16:", math.sqrt(16))
print("Factorial of 5:", math.factorial(5))
print("Logarithm of 10:", math.log10(10))
print("Power 2^3:", math.pow(2, 3))
print("Cosine of 0 degrees:", math.cos(math.radians(0)))
print("Sine of 90 degrees:", math.sin(math.radians(90)))
print("Greatest Common Divisor (GCD) of 8 and 12:", math.gcd(8, 12))
print("Pi constant:", math.pi)
print("Euler's number:", math.e)
